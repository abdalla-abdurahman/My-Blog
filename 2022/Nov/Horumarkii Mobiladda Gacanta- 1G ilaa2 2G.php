        <h2 class="sub-heading"></h2>
        <p class="post-text">Tiknoolajiyada mobiladu waxay soo martay jiilal kala duwan intii ay jirtay. Jiilashaa (Generations) oo loo soo gaabiyo xarafka G sida 1G, 2G, 3G, 4G iyo 5G, waxa ay ku kala duwan yihiin adeegyada iyo qaabdhismeedka network-ga. Mobile Phone ama Cell Phone sida meelaha qaar looga yaqaano waa aalad qofku sidan karo oo loo isticmaalo in ay dadku ku wada xidhiidhan cod (voice), qoraal (text) ama muuqaal (video) sida hadda jirta.</p>

        <img src="../../img/Nokia 3210.JPG" alt="" srcset="" class="post-img"> <br><br>
        <p class="post-text">
            Hadaba, 1G oo ahaa jiilka ugu horeeyay ee telefoonada gacanta lagu qaato waxa la hirgaliyay isticmaalkiisa 1980. Maadama oo bilaw ahayd tiknoolajiyadani, wadamo badan oo ku hawlanaa tiknoolajiyadani ayaa mid kastaaba iskii u samaystay. Wadamadaa Nordic ee yurubta waqooyi waxay samaysteen NMTs (Nordic Mobile Telephones), Uk Total Access Comunication System (TACS), sidoo kale U.S. Advance Mobile Phone System (AMPS).
            1G waxa uu ahaa analogy system oo keena in tayada codku xumaado markasta oo masaafadu sii dheerato iyo in signalka uu ku bato waxa loo yaqaano interference. Dhibta kale uu laha ayaa waxa ka mida ma ay isticmaali karayn dad badani hal mar. Tusaale; hadii ay isku dayaan dad badani inay hal mar wicitaan sameeyaan ma ay u suurto geli karayn, sababta oo ah waxa xadidnaa janallada loogu talo galay in ay qaadan hadalka, dadka Frequency Division Multiple Access (FDMA). Ta kale, is ma ay geli karayn systems ka kala duwan ee wadan kasta u lahaa sida qofka UK jooga ee isticmaalaya TACS lama u xidhiidhi karayn ka U.S. jooga ee isticmaalaya AMPS. </p>

        <img src="../../img/nokia mobira senator.jpg" alt="" srcset="" class="post-img"> <br><br>
        <br>
        <p class="post-text">
            Waxa hadaba muhiim noqotay si loo yareeyo dhibaatoyinka u lahaa 1G in la sameeyo jiilka labaad ee ama second generation (2G). Second generation waxa u xaliyay dhibaatoyin badan sida in uu kordhiyay tirada subscribers ka ee mar keliya isticmaali kara, in tayada codka uu fiicnaado iyada oo loo badalayo digital signal. Adeega fariimaha qoraalka ee Short Message Service (SMS) ayaa isna ka mid ahaa adeegyada uu 2G-gu soo kordhiyay. Waxa kale markii danbe lagu soo daray adeega loo yaqaano General Packet Radio System (GPRS) oo u saamaxay in lagu isticmaalo mobile-ka internet-ka markii ugu horraysay u soo kordhiyay adeegyo cusub.
            <br>

            Intaas oo faa’ido oo 2G-gu soo kordhiyay ayaa keentay in markiiba laga guuro system kii horee jiray ee 1G. Ka hore intii aan la hirgalin samaynta 2G-ga, Europe oo badanka hogaamiysay dhanka tiknoolajiyadda mobillada, ayaa isku raacday in ay sameeyan hay’ad u qaabilsan dejinta nidaamyada iyo protocols-ka uu lahaanayo jiilka labaad ee mobiladu halkii markii hore wadan kastaaba iskii u samysanayay. Hay’adan oo lagu magacaabo European Telecommunications Standards Institute (ETSI) waxa ay soo saartay Global System for Mobile Communication (GSM) oo ah hab qeexaaya sida uu shaqaynayo iyo astaamaha uu leeyahay 2G-gu. Taasi waxay suurto galisay adeega la yidhaa roaming-ka oo 1G suurto gal ahayn maadaama oo ay kala duwanaayeen hirarka (frequency) ay kala isticmaalayaan systemyadu, in uu noqdo faaido kale oo uu keenay GSM.
            Hase ahaate, North America iyadu waxa ay si horumarisay AMPS kana dhigtay 2G oo noqday D-AMPS. Inta kalee aduunka waxay wada qaateen GSM-ka.

        </p>

